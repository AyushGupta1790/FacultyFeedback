    private void jLabel11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel11MouseClicked
        // TODO add your handling code here:
        try {
            int count = 0, score = 0;
            String fname, course, sem, sub, branch;
            fname = course = sem = sub = branch = null;
            DBConnection db = new DBConnection();
            Vector<String> header = new Vector<>();
            Vector<Vector<String>> data = new Vector<Vector<String>>();
            header.add("Faculty Id");
            header.add("Faculty Name");
            header.add("Subject Name");
            header.add("Course");
            header.add("Semester");
            header.add("Branch");
            header.add("Performance");
            db.pstmt = db.con.prepareStatement("select * from subject_allotment");
            db.rst = db.pstmt.executeQuery();
            while (db.rst.next()) {
                Vector<String> temp = new Vector<>();
                temp.add(db.rst.getString(2));
                fname = db.rst.getString(3);
                temp.add(db.rst.getString(3));
                sub = db.rst.getString(4);
                temp.add(db.rst.getString(4));
                course = db.rst.getString(5);
                temp.add(db.rst.getString(5));
                sem = db.rst.getString(7);
                temp.add(db.rst.getString(7));
                branch = db.rst.getString(6);
                temp.add(db.rst.getString(6));
                db.pstmt = db.con.prepareStatement("select * from fac_feed_ans where course=? and sem=? and faculty=? and subject=?");
                db.pstmt.setString(1, course);
                db.pstmt.setString(2, sem);
                db.pstmt.setString(3, fname);
                db.pstmt.setString(4, sub);
                db.rst1 = db.pstmt.executeQuery();
                while (db.rst1.next()) {
                    for (int i = 4; i < 19; i++) {
                        score += db.rst1.getInt(i);
                    }
                    count++;
                }
                if (score != 0) {
                    score = (score) / count;

                    if (score >= 67) {
                        temp.add("Excellent");
                    } else if (score >= 52) {
                        temp.add("Good");
                    } else if (score >= 37) {
                        temp.add("Average");
                    } else if (score >= 23) {
                        temp.add("Poor");
                    } else {
                        temp.add("Very Poor");
                    }
                } else {
                    temp.add("N/A");
                }
                data.add(temp);

            }
            jTable1.setModel(new DefaultTableModel(data, header));
            FeedbackReport.setVisible(true);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jLabel11MouseClicked

