            if (course.equals("")) {
                JOptionPane.showMessageDialog(this, "course can not be empty");
                return;
            }
            String dept = jTextField5.getText();
            if (dept.equals("")) {
                JOptionPane.showMessageDialog(this, "Department can not be empty");
                return;
            }
            String spec = jTextField6.getText();
            if (spec.equals("")) {
                JOptionPane.showMessageDialog(this, "Specialization can not be empty");
                return;
            }
            String uid = jTextField23.getText();
            if (uid.equals("")) {
                JOptionPane.showMessageDialog(this, "Login id can not be empty");
                return;
            }
            String pswd = jTextField24.getText();
            if (pswd.equals("")) {
                JOptionPane.showMessageDialog(this, "Password can not be empty");
                return;
            }

            db.pstmt = db.con.prepareStatement("insert into faculty_info values(?,?,?,?,?,?)");
            db.pstmt.setString(1, fid);
            db.pstmt.setString(2, fname);
            db.pstmt.setString(3, course);
            db.pstmt.setString(4, dept);
            db.pstmt.setString(5, spec);
            db.pstmt.setString(6, uid);

